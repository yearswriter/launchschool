def tricky_method(a_string_param, an_array_param)
  puts "Passed value in the param \"a_string_param\" with value of #{a_string_param} and id of #{a_string_param.object_id}"
  puts "Passed value in the param \"an_array_param\" with value of #{an_array_param} and id of #{an_array_param.object_id}"
  a_string_param += "rutabaga" # << will make it behave like an array below (change initial object)
  puts "Changed value in the param \"a_string_param\" with value of #{a_string_param} and id of #{a_string_param.object_id}"
  an_array_param << "rutabaga" # += will make it behave like a string above (creat new local object and change it instead)
  puts "Changed value in the param \"an_array_param\" with value of #{an_array_param} and id of #{an_array_param.object_id}"
end

my_string = "pumpkins"
puts "My string value: #{my_string} with id #{my_string.object_id}"
my_array = ["pumpkins"]
puts "My array value: #{my_array} with id #{my_array.object_id}"
tricky_method(my_string, my_array)
puts "My string looks like this now: #{my_string} with id #{my_string.object_id}"
puts "My array looks like this now: #{my_array} with id #{my_array.object_id}"

